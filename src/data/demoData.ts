export const getCurrencyRate = (code: string): number => {
  const currency = currencies.find(c => c.code === code);
  return currency?.rateToINR ?? 1;
};

export const convertToINR = (amount: number, currencyCode: string): number => {
  return amount * getCurrencyRate(currencyCode);
};

export const formatCurrency = (amount: number, currencyCode: string): string => {
  const currency = currencies.find(c => c.code === currencyCode);
  const symbol = currency?.symbol ?? '₹';
  return `${symbol}${amount.toLocaleString('en-IN')}`;
};

export const formatINR = (amount: number): string => {
  return `₹${amount.toLocaleString('en-IN')}`;
};

export const getCitiesByCountry = (countryId: string): City[] => {
  return cities.filter(c => c.countryId === countryId);
};

export const getCountryCurrency = (countryId: string): string => {
  const country = countries.find(c => c.id === countryId);
  return country?.defaultCurrency ?? 'INR';
};
